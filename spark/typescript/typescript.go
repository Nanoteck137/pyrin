	// body := g.mapName(e.Body)

	w.IndentWritef("%s", name)
	w.Writef("(")

	for _, arg := range args {
		w.Writef("%s: string, ", arg)
	}

	w.Writef("body: FormData, ")

	w.Writef("options?: ExtraOptions")

	w.Writef(") {\n")

	w.Indent()

	w.IndentWritef("return this.requestForm(")

